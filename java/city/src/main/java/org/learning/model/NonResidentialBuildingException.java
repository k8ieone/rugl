package org.learning.model;

public class NonResidentialBuildingException extends Exception{

    public NonResidentialBuildingException() { super(); }
    public NonResidentialBuildingException(String message) { super(message); }
    public NonResidentialBuildingException(String message, Throwable cause) { super(message, cause); }
    public NonResidentialBuildingException(Throwable cause) { super(cause); }
}
